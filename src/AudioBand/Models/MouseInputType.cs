namespace AudioBand.Models
{
    /// <summary>
    /// The mouse input type related to a MouseBinding.
    /// </summary>
    public enum MouseInputType
    {
        /// <summary>
        /// Left mouse click.
        /// </summary>
        LeftClick = 0,

        /// <summary>
        /// Double left mouse click.
        /// </summary>
        LeftDoubleClick = 1,

        /// <summary>
        /// Middle mouse click.
        /// </summary>
        MiddleClick = 2,

        /// <summary>
        /// Double middle mouse click.
        /// </summary>
        MiddleDoubleClick = 3,

        /// <summary>
        /// Scrolling up.
        /// </summary>
        ScrollUp = 4,

        /// <summary>
        /// Scrolling down.
        /// </summary>
        ScrollDown = 5,
    }
}
